{

}

stringT CubeComponent::GetTypeName()
{
	return _T("CubeComponent");
}

void CubeComponent::Render()
{
	if (GetOwner()) {
		if (GetOwner()->GetHighLight()) {
			glEnable(GL_CULL_FACE);
			glCullFace(GL_FRONT);
			glLineWidth(2.0f);
			glPolygonMode(GL_FRONT_AND_BACK, GL_LINE);
			ResourceManager::getInstance()->GetShader("outline").use().setMatrix4("model", GetOwner()->GetWorldTransform());
			glBindVertexArray(m_VAO);
			glDrawArrays(GL_TRIANGLES, 0, 36);
			glBindVertexArray(0);
		}

		glDisable(GL_CULL_FACE);
		glLineWidth(1.0f);
		glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);
		m_Shader.use();
		if (m_UseTexture) {
			glActiveTexture(GL_TEXTURE0);
			glBindTexture(GL_TEXTURE_2D, m_Texture);
		}
		m_Shader.setInt("useTexture", m_UseTexture);
		m_Shader.setMatrix4("model", GetOwner()->GetWorldTransform());
		m_Shader.setVec3("cubeColor", m_Color);
		m_Shader.setInt("highLight", GetOwner()->GetHighLight());
		m_Shader.setVec3("highLightColor", GetOwner()->GetHighLightColor());

		glBindVertexArray(m_VAO);
		glDrawArrays(GL_TRIANGLES, 0, 36);
		glBindVertexArray(0);
	}
}

void CubeComponent::Render(Shader shader)
{
	if (GetOwner()) {
		shader.use();
		shader.setMatrix4("model", GetOwner()->GetWorldTransform());

		glBindVertexArray(m_VAO);
		glDrawArrays(GL_TRIANGLES, 0, 36);
		glBindVertexArray(0);
	}
}

void CubeComponent::SetColor(const glm::vec3& color)
{
	m_Color = color;
}
